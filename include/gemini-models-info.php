<h2>GEMINI GATE MOTORS</h2>


<h3>GEMINI DC SLIDER</h3>
<p>
The GEMINI 12V dc domestic sliding gate system is a self contained unit,
which comprise of a single 12 Volt direct current electrical motor that
drives the rack pinion through a reduction gearbox. The electrical motor,
carrier bearing and associated components are mounted in a rust treated base box.
The base box is secured to the base plate, which in turn is cemented or bolted to a concrete base.
The complete unit is enclosed with a weather resistant lid. This motor is available in three models.
</p>
<p>
	<ul>
		<li>00120 GEMINI DC-Slider Domestic - 300kg</li>
		<li>00130 GEMINI DC-Slider 7.5aH - 600kg</li>
		<li>00140 GEMINI DC-Slider 12aH - 1200kg</li>
	</ul>
</p>
<br/><br/>


<h3>GEMINI AC SLIDER</h3>
<p>
The GEMINI 220 V sliding gate system is a self contained unit which comprise of a single 220 Volt AC
electrical motor that drives the rack pinion via a gearbox. The gearbox, electrical motor and associated
components are mounted in a rust treated base box. The base box is secured to the base plate, which in
turn is cemented or bolted to a concrete base. The complete unit is enclosed with a weather resistant lid.
The AC Slider is available in two models.
</p>
<p>
	<ul>
		<li>00150 GEMINI AC-Slider Domestic - 400kg</li>
		<li>00160 GEMINI AC-Slider - 1200kg</li>
		<li>00170 GEMINI Industrial Slider - 2500kg</li>
	</ul>
</p>
<br/><br/>


