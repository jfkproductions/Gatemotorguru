<h1>Different types of Remotes and Compatibility with Gate Motors</h1>

<h2>Types of Remotes</h2>
<p>
The most primitive (and least secure), are the remotes that use a fixed code, that is usually determined by flipping small "dip" switches inside
the remote to a specific combination
</p>

<p>
    The next generation of remotes are various forms of rolling pin and crypto-key remotes, that use sophisticated encryption algorithms ("programs")
    to create unique non-duplicatable codes. Some types of remotes, can thus not be cloned, and not be picked up by burglars using radio frequency scanners.
</p>

<h3>The Green "Smart" Remote</h3>
<p>
This type of remote use a fixed unique code. These kinds of remotes can be cloned. If you need another remote, you take it to a shop, and they
can copy the code onto a new remote for you. A sophisticated burglar using a radio scanner could also clone your remote in theory, although this
happens very rarely in practice.
</p>

<h3>The Blue "Nova" Remote</h3>
<p>
This remote has a unique randomly changing code, which makes it impossible to duplicate. This is the most secure type of remote.
These remotes are paired (synchronised) with a specific gate motor's receiver unit.
Each gate's receiver unit keeps track of which remotes have been added to it. To configure a new remote, you need to follow the instructions of
your particular gate motor's receiver unit, to add a remote to it.
</p>

