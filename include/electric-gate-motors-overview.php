<h3>Centurion D10 Industrial Strength Electric Gate Motors </h3>
<p>
D10 - Using CENTURION’s proven DC technology, this is the complete solution for commercial and industrial gates up to 1000kg.
</p>
<br/><br/>

<h3>Centurion A10 Industrial Electric Gate Motor </h3>
<p>
Capable of handling sliding gates up to 2 tonnes in mass, and in arduous operating conditions, the automatic choice for commercial and 
industrial applications is the A10.
</p>
<p>
The A10 uses a 3-phase AC motor and electronic inverter to provide ultra rapid opening and closing of the gate. 
The speed can be increased from 16 up to 30 metres per 
minute and is adjustable between opening and closing. 
</p>
