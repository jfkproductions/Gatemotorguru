<h2>Centurion brand Electric Gate Motors</h2>

The Centurion brand of electric gate motors are well known for their reliablity, performance and good design - you almost cannot go wrong with a Centurion Electric Gate Motor.
They are well supported in South Africa, and spares are generally available.
<br/>

<h3>Centurion D3 Electric Gate Motor </h3>
<p>
Capable of opening an average 4m gate in less than 14 seconds, and this combined with CENTURION’s unique PIRAC 
(passive infrared auto close) mode that closes the gate the moment a vehicle has driven through the entrance, 
it offers the highest level of security and convenience for any home. In addition, these electric gate motors come standard with battery-back up 
ensuring that you’ll never be left outside in the event of a power failure.
</p>
<p>
The D3 sliding gate motor is a cost-effective electric gate motor for domestic installations, designed to operate gates weighing up to 300kg.
</p>
